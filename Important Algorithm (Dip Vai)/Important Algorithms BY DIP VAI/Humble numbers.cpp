priority_queue<LL>q;

void bt(LL num,int st)
{
    LL i,tem;
    fr(i,st,k)
    {
        tem=num*pri[i];
        if(tem>=lim)
            break;
        q.push(tem);
        if(q.size()>n)
        {
            if(q.top()==tem)
            {
                q.pop();
                break;
            }
            q.pop();
        }
        bt(tem,i);
    }

}


